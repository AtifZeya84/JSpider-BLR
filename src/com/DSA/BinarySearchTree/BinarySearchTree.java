package com.DSA.BinarySearchTree;

public class BinarySearchTree {
    Node root ;
    int count = 0;
    boolean flag;

     public boolean add(int key){
         flag = true;
         root = add(root,key);
         return flag;
     }

     public Node add(Node n, int key){
         if (n == null){
             n = new Node(key);
             count++;
             return n;
         }
         if(key < n.key)  n.left = add(n.left,key);
         else if (key > n.key)  n.right = add(n.right,key);
         else  flag = false;
         return  n;
     }

     public int size(){
         return count;
     }

     public void postOrder(){
         preOrder(root);
     }
    public void preOrder(){
        postOrder(root);
    }
    public void inOrder(){
        inOrder(root);
    }

    public void preOrder(Node n){
        if (n == null) return;
        System.out.print(n.key + " ");
        preOrder(n.left);
        preOrder(n.right);
     }
    public void postOrder(Node n){
        if (n == null) return;
        postOrder(n.left);
        postOrder(n.right);
        System.out.print(n.key + " ");
    }
    public void inOrder(Node n){
        if (n == null) return;
        inOrder(n.left);
        System.out.print(n.key + " ");
        inOrder(n.right);
    }
}
